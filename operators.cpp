#include <iostream>
using namespace std;

int main()
{

    int x =5, y=2;

    cout<<"x/y = "<<x/y<<endl;
    cout<<"x%y = "<<x%y<<endl;

    /* will display 2 */
    /* will display 1, the remainder of
    the integer division */

    x = 1;
    y = 2;
    cout<<"x/y= "<<x/y<<endl<< "x%y = "<< x%y<<endl;
    /* will display 0 1 */

    return 0;
}

